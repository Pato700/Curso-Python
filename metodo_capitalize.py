"""
El método capitalize se utiliza para convertir el primer carácter de un string en mayúscula, 
y a su vez, todas las letras restantes en minúsculas.
"""

string = 'el VIAJE es la Recompensa'
print(f"Antes de aplicar capitalize(): {string}")
string = string.capitalize()
print(f"Después de aplicar capitalize(): {string}")
