"""
El m√©todo reverse se encarga de invertir los elementos de una lista en su mismo lugar,
modifica las pocisiones de la lista, en lugar de crear una nueva lista
"""

vocales = ["a", "e", "i", "o", "u"]
print(f"Lista: {vocales}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"Lista: {vocales}")
print(f"pocision 0: {vocales[0]}")

vocales.reverse()
print(f"\nLista: {vocales}")  
print(f"pocision 0: {vocales[0]}")
